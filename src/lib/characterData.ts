export function generateName(gender: string, worldview: string): string {
  if (worldview === "西幻魔法") {
    return pick(westernSurnames) + pick(westernGiven);
  }
  const surname = pick(surnames);
  if (gender === "女") return surname + pick(givenFemale);
  if (gender === "男") return surname + pick(givenMale);
  return surname + pick([...givenMale, ...givenFemale, ...givenNeutral]);
}

export function generateCharacter(opts: { worldview?: string; occupation?: string; gender?: string }) {
  const wv = opts.worldview || pick(Object.keys(worldData));
  const data = worldData[wv];
  const g = opts.gender || pick(["男", "女", "非二元"]);
  const identity = opts.occupation
    ? `${opts.occupation}`
    : pick(data.identities);
  const name = generateName(g, wv);

  return {
    姓名: name,
    世界观: wv,
    身份: identity,
    角色定位: pick(rolePositions),
    性格特质: pick(personalities),
    外貌特征: pick(data.appearances),
    外在气质: pick(data.auras),
    兴趣偏好: pick(data.interests),
    成长背景: pick(data.backgrounds),
    核心矛盾: pick(data.conflicts),
    人物秘密: pick(data.secrets),
    剧情钩子: pick(data.hooks),
  };
}

